package com.system.day.DTO;

public class AtividadeHorario {

	private Long idAtividade;
	
	private Long idHorarioInicio;
	
	private Long idHorarioFim;

	public Long getIdAtividade() {
		return idAtividade;
	}

	public void setIdAtividade(Long idAtividade) {
		this.idAtividade = idAtividade;
	}

	public Long getIdHorarioInicio() {
		return idHorarioInicio;
	}

	public void setIdHorarioInicio(Long idHorarioInicio) {
		this.idHorarioInicio = idHorarioInicio;
	}

	public Long getIdHorarioFim() {
		return idHorarioFim;
	}

	public void setIdHorarioFim(Long idHorarioFim) {
		this.idHorarioFim = idHorarioFim;
	}
	
	
	
}
